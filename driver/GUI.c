/**************************************************************************************
功能描述: 数码管数字（支持直接显示字符串 如 "11:12" "12:33"）
输    入: x、y起点坐标，fc文字颜色，bc背景色，str要显示的字符串（如"11:12"）
输    出: 无
**************************************************************************************/
void Gui_DrawFont_Num24x48(u16 x, u16 y, u16 fc, u16 bc, const char *str)
{
    unsigned char i,j,k,c;
    u16 cur_x = x;           // 当前绘制X坐标
    char ch;                 // 当前字符

    // 仍然使用你原来 32x32 字模
    #define FONT_H    32
    #define FONT_W    32
    #define BYTE_W    4

    // 循环显示字符串里的每一个字符
    while((ch = *str++) != 0)
    {
        unsigned int index = 0; // 字模索引

        // ========== 字符对应字模库索引 ==========
        if(ch >= '0' && ch <= '9')
        {
            index = ch - '0';     // 0-9 → 索引0-9
        }
        else if(ch == '.')
        {
            index = 10;
        }
        else if(ch == ':')
        {
            index = 11;           // : → 索引11
        }
        else if(ch == '%')
        {
            index = 12;
        }
        else if(ch == '℃')
        {
            index = 13;
        }
        else if(ch == '-')
        {
            index = 14;
        }
		else if(ch == ' ')
        {
            index = 15;
        }
        else
        {
            index = 0; // 不支持的字符默认显示0
        }

        // ========== 正常读取你的 32x32 字模 ==========
        for(i=0;i<FONT_H;i++)
        {
            for(j=0;j<BYTE_W;j++)
            {
                c=*(sz32 + index*FONT_H*BYTE_W + i*BYTE_W + j);
                for (k=0;k<8;k++)
                {
                    // 只画前24列，避免超出
                    if(j*8 + k >= 24) continue;

                    if(c&(0x80>>k))
                        Gui_DrawPoint(cur_x + j*8 +k, y+i, fc);
                    else if(fc != bc)
                        Gui_DrawPoint(cur_x + j*8 +k, y+i, bc);
                }
            }
        }
        // 每个字符只占24宽度，实现24*48效果
        cur_x += 24;
    }
}


/**************************************************************************************
功能描述: 任意位置显示任意大小图片
输    入: x - 起点x ；y - 起点y ；c - 图片长度 ；k - 图片宽度；p - 图片数组;
输    出: 无
**************************************************************************************/

void Gui_showimage(const unsigned char *p, uint8_t c, uint8_t k, uint8_t x, uint8_t y)
{
	uint32_t i, picL, picH;
	Lcd_SetRegion(x+2,y,x+(c - 1) ,y + (k - 1));		//坐标设置
    for(i=0;i<c*k;i++)
	 {	
	 	picL=*(p+i*2);	//数据低位在前
		picH=*(p+i*2+1);				
		LCD_WriteData_16Bit(picH<<8|picL);  						
	 }		
}

// 8*16 ASCII 字符显示（数字、字母、符号）
void Gui_DrawFont_ASCII8x16(u16 x, u16 y, u16 fc, u16 bc, u8 ch)
{
	u8 i,j;
	u16 k;

	if(ch < 32) ch = 32;  	 // 不可见字符替换为空格
	if(ch > 126) ch = 126;   // 超出范围限制
	k = ch - 32;             // 字库偏移

	for(i=0;i<16;i++)        // 高度16
	{
		for(j=0;j<8;j++)     // 宽度8
		{
			if(asc16[k*16 + i] & (0x80 >> j))
				Gui_DrawPoint(x+j, y+i, fc);
			else
			{
				if(fc != bc)
					Gui_DrawPoint(x+j, y+i, bc);
			}
		}
	}
}

// 8*16 字符串显示（英文、数字、符号通用）
void Gui_DrawString_8x16(u16 x, u16 y, u16 fc, u16 bc, u8 *str)
{
	while(*str)
	{
		Gui_DrawFont_ASCII8x16(x, y, fc, bc, *str);
		x += 8;         // 每个字符占8像素宽度
		str++;
	}
}


